import java.util.*;
import javafx.geometry.Point2D;

/**
 * Need to import javafx
 */
public class Point2DDemo {
    public static void main(String[] args) {

        // a Point2D object represents the a point with x and y coordinates
        double x1 = 2.5;
        double y1 = 3.2;
        double x2 = 5.6;
        double y2 = 6.7;

        Point2D point1 = new Point2D(x1, y1);
        Point2D point2 = new Point2D(x2, y2);

        System.out.println(point1.distance.point2);

    }
}